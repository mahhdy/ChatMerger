<!-- الف-۳ عرفان -->
<h3>الف.۳ آزادی در عرفان اسلامی&ndash;ایرانی</h3>

<p>
  عرفان اسلامی رادیکال‌ترین تعبیر آزادی را در سنت اسلامی عرضه کرد:
  <strong>آزادی = رهایی از بندِ &laquo;من&raquo;</strong>. نَفس (ego) بزرگ‌ترین زندان است
  و فنای در خدا (fan&#257;&#x02BC; f&#299; All&#257;h) عالی‌ترین آزادی.
</p>

