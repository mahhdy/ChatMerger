</p>

<div class="card accent-gold">
  <h4>&#9878; تقابل ایرانشهری: عدالت vs ستم (نه آزادی vs بردگی)</h4>
  <ul>
    <li>در اندیشه‌ی ایرانشهری، مفهوم محوری نه <em>آزادی</em> (eleutheria) بلکه <strong>عدالت</strong> (<em>a&scaron;a / d&#257;d</em>) در برابر <strong>ستم</strong> (<em>druj / b&#299;d&#257;d</em>) بود.</li>
    <li><strong>کوروش بزرگ</strong> (منشور کوروش، ~539 ق.م): آزادی دینی، بازگرداندن مردمان تبعیدشده، ممنوعیت بیگاری &mdash; اینها ذیل <em>عدالت شاهانه</em> قرار داشتند نه &laquo;حقوق طبیعی&raquo;.</li>
