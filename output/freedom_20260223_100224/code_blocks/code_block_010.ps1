<h4>الف.۲.۱ معتزله: عدالت الهی مستلزم اختیار انسان</h4>
<p>
  معتزله (قرن ۲&ndash;۳ هجری / ۸&ndash;۹ میلادی) استدلال کردند:
  اگر خداوند <em>عادل</em> (اصل عدل) است، باید انسان <em>مختار</em> باشد.
  پاداش و کیفر بدون اختیار بی‌معناست. پس انسان <strong>خالق افعال خویش</strong> است
  (<em>khalq al-af&#x02BB;&#257;l</em>). این موضع نزدیک‌ترین موضع اسلامی به
  &laquo;آزادی اراده&raquo; در سنت غربی است.
</p>

<h4>الف.۲.۲ اشاعره: نظریه‌ی کسب</h4>
<p>
  ابوالحسن اشعری (م. ۹۳۶) راه سومی پیشنهاد کرد: خداوند <em>خالق</em> فعل است
  ولی انسان <em>کاسب</em> (&#1603;&#1575;&#1587;&#1576;) آن. فعل مخلوق خداست ولی انسان
  آن را &laquo;کسب&raquo; می‌کند. این نظریه تلاشی برای حفظ هم <em>قدرت مطلق خدا</em>
  و هم <em>مسئولیت انسان</em> بود، اگرچه منتقدان (معتزله، فلاسفه) آن را
  &laquo;جبر پنهان&raquo; نامیدند.
</p>

<h4>الف.۲.۳ شیعه: امر بین الامرین</h4>
<p>
  منسوب به امام جعفر صادق: <strong>&laquo;لا جبر و لا تفویض بل امر بین الامرین&raquo;</strong>
  &mdash; نه جبر محض و نه واگذاری مطلق، بلکه امری میان این دو.
  این موضع نوعی <em>سازگارگرایی</em> (compatibilism) اسلامی است:
  انسان در چارچوب مشیت الهی <em>مختار</em> است.
</p>

